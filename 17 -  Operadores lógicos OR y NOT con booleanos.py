# Operadores de comparacion

x = 5
y = 3


print(x == y)  # Igual a
print(x != y)  # Diferente de
print(x > y)   # Mayor que
print(x < y)   # Menor que
print(x >= y)  # Mayor o igual que
print(x <= y)  # Menor o igual que

# Operadores logicos and, or y not

print(x > 2 and y < 5)  # AND: Verdadero si ambas condiciones son verdaderas
print(x > 2 or y > 5)   # OR: Verdadero si alguna de las condiciones es verdadera
print(not(x > 2))       # NOT: Invierte el valor booleano de la condicion